package mapeamento;

/**
 *
 * @author Gus
 */
public class Tomates {
    String nomeArquivo;
    int vermelhos;
    int verdes;
    int pretos;
    int estado;
    int numTom;
    int rua;
    String linha;
    String lat;
    String longi;
    String data;

    public String getNomeArquivo() {
        return nomeArquivo;
    }

    public void setNomeArquivo(String nomeArquivo) {
        this.nomeArquivo = nomeArquivo;
    }

    public int getVermelhos() {
        return vermelhos;
    }

    public void setVermelhos(int vermelhos) {
        this.vermelhos = vermelhos;
    }

    public int getVerdes() {
        return verdes;
    }

    public void setVerdes(int verdes) {
        this.verdes = verdes;
    }
    
    public int getPretos() {
        return pretos;
    }

    public void setPretos(int pretos) {
        this.pretos = pretos;
    }

    public int getEstado() {
        return estado;
    }

    public void setEstado(int estado) {
        this.estado = estado;
    }

    public int getNumTom() {
        return numTom;
    }

    public void setNumTom(int numTom) {
        this.numTom = numTom;
    }

    public int getRua() {
        return rua;
    }

    public void setRua(int rua) {
        this.rua = rua;
    }

    public String getLinha() {
        return linha;
    }

    public void setLinha(String linha) {
        this.linha = linha;
    }

    public String getLat() {
        return lat;
    }

    public void setLat(String lat) {
        this.lat = lat;
    }

    public String getLongi() {
        return longi;
    }

    public void setLongi(String longi) {
        this.longi = longi;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }
    
}
